package com.mockito.demo;

public class Calculator {

	public int addTwo(int i, int j) {
		return i + j;
	}

	public int multiplyTwo(int i, int j) {
		return i * j;
	}

	public int divideTwo(int i, int j) {
		return i / j;
	}

	public int substractTwo(int i, int j) {
		return i - j;
	}

	public int perform(int i, int j) {
		return (i + j) * i;
	}

}
